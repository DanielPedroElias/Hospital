    private void jButtonSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvaActionPerformed
        // TODO add your handling code here:
        // Extrair os valores dos campos de texto
    String nome = txtNome.getText();
    double mensalidade = Double.parseDouble(txtMensalidade.getText());

    // Criar um novo objeto Plano com os valores dos campos de texto
    Plano plano = new Plano(nome, mensalidade);

    try {
        // Tentar salvar o plano no banco de dados
        PlanoPersistencia.salvar(plano);
        // Se não houver exceção, o salvamento foi bem-sucedido
        JOptionPane.showMessageDialog(this, "Plano salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception ex) {
        // Se uma exceção for lançada, exibir mensagem de erro
        JOptionPane.showMessageDialog(this, "Erro ao salvar o plano: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonSalvaActionPerformed

    private void jButtonRecuperaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRecuperaActionPerformed
        // TODO add your handling code here:
        // Extrair o nome do plano a ser recuperado
    String nome = txtNome.getText();

    // Tentar recuperar o plano do banco de dados
    Plano plano = PlanoPersistencia.recuperar(nome);

    // Verificar se o plano foi encontrado
    if (plano != null) {
        // Se o plano foi encontrado, exibir os detalhes na interface do usuário
        JOptionPane.showMessageDialog(this, "Nome: " + plano.getNome() + "\nMensalidade: " + plano.getMensalidade(), "Plano Encontrado", JOptionPane.INFORMATION_MESSAGE);
    } else {
        // Se o plano não foi encontrado, exibir uma mensagem informando ao usuário
        JOptionPane.showMessageDialog(this, "Plano não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonRecuperaActionPerformed
    
    
