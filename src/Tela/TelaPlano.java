    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        // TODO add your handling code here:
        // Extrair os valores dos campos de texto
        String nomeAntigo = txtNome.getText();
        String novoNome = txtNovoNome.getText();
        double novaMensalidade = Double.parseDouble(txtMensalidade.getText());

        // Tentar recuperar o plano do banco de dados pelo nome antigo
        Plano plano = PlanoPersistencia.recuperar(nomeAntigo);

        // Verificar se o plano foi encontrado
        if (plano != null) {
            // Se o plano foi encontrado, atualizar os valores e tentar atualizar no banco de dados
            plano.setNome(novoNome);
            plano.setMensalidade(novaMensalidade);
            PlanoPersistencia.atualizar(plano, novoNome, novaMensalidade);
            JOptionPane.showMessageDialog(this, "Plano atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Se o plano não foi encontrado, exibir uma mensagem de erro
            JOptionPane.showMessageDialog(this, "Plano não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        // Recupera o nome do plano a ser excluído do campo de texto
    String nomePlano = txtNome.getText();

    // Verifica se o plano existe no banco de dados
    Plano plano = PlanoPersistencia.recuperar(nomePlano);
    if (plano != null) {
        // Se o plano existir, chama o método para excluí-lo
        PlanoPersistencia.excluir(nomePlano);
        JOptionPane.showMessageDialog(this, "Plano excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    } else {
        // Se o plano não for encontrado, exibe uma mensagem de erro
        JOptionPane.showMessageDialog(this, "Plano não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    
    }//GEN-LAST:event_jButtonExcluirActionPerformed

